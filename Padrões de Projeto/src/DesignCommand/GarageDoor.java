package DesignCommand;

public class GarageDoor {
	
	public void up() {
		System.out.println("Abrir Porta!");
	}
	public void down() {
		System.out.println("Fechar Porta!");
	}
	public void stop() {
		System.out.println("Parar Porta!");
	}
	public void lightOn() {
		System.out.println("Ligar Luz!");
	}
	public void lightOff() {
		System.out.println("Desligar Luz!");
	}
	
}
